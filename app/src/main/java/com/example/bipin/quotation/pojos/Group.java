public class Group {

    private String name, code, date;

    public Group(String name, String code, String date) {
        this.name = name;
        this.code = code;
        this.date = date;

    }

    public String getName() {
        return name;
    }

    public String getCode() {
        return code;
    }

    public String getDate() {
        return date;
    }

    }



